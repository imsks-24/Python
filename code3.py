# if-else
print("Enter the value of h : ")
h = int(input())
if (h > 10):
    # tab is important for writing the program inside the if.
    print("The no. is greater than 10")
elif (h < 10):  # else if
    print("The no. is less than 10")
else:
    print("Equal")

